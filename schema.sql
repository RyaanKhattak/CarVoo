-- Profiles Policies
CREATE POLICY "Public profiles are viewable by everyone" ON public.profiles FOR SELECT USING (true);
CREATE POLICY "Users can insert their own profile" ON public.profiles FOR INSERT WITH CHECK (auth.uid() = id);
CREATE POLICY "Users can update their own profile" ON public.profiles FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Admins can manage all profiles" ON public.profiles FOR ALL USING (public.is_admin());

-- Product Policies
CREATE POLICY "Approved products are viewable by everyone" ON public.products FOR SELECT USING (status = 'approved');
CREATE POLICY "Sellers can view their own products" ON public.products FOR SELECT USING (auth.uid() = seller_id);
CREATE POLICY "Sellers can insert their own products" ON public.products FOR INSERT WITH CHECK (auth.uid() = seller_id);
CREATE POLICY "Sellers can update their own products" ON public.products FOR UPDATE USING (auth.uid() = seller_id);
CREATE POLICY "Sellers can delete their own products" ON public.products FOR DELETE USING (auth.uid() = seller_id);
CREATE POLICY "Admins can manage all products" ON public.products FOR ALL USING (public.is_admin());

-- Order Policies
CREATE POLICY "Orders access policy" ON public.orders FOR SELECT USING (
    public.is_admin() OR 
    auth.uid() = buyer_id OR 
    public.is_order_seller(id)
);

CREATE POLICY "Buyers can create their own orders" ON public.orders FOR INSERT WITH CHECK (auth.uid() = buyer_id);
CREATE POLICY "Admins can update orders" ON public.orders FOR UPDATE USING (public.is_admin());

-- Order Items Policies
CREATE POLICY "Order items access policy" ON public.order_items FOR SELECT USING (
    public.is_admin() OR 
    public.is_order_buyer(order_id) OR
    EXISTS (SELECT 1 FROM public.products WHERE id = product_id AND seller_id = auth.uid())
);

CREATE POLICY "Admins can manage order items" ON public.order_items FOR ALL USING (public.is_admin());

-- Review Policies
CREATE POLICY "Reviews are viewable by everyone" ON public.reviews FOR SELECT USING (true);
CREATE POLICY "Authenticated users can create reviews" ON public.reviews FOR INSERT WITH CHECK (auth.role() = 'authenticated');

-- Cart Policies
